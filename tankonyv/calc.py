def addition(x, y):
    """
    Összeadás

    :param x:az első szám
    :type x: int
    :param y: a második szám
    :type y: int
    :return: eredmény
    :rtype: int

    """
    return x + y


def subtraction(x, y):
    """
    Kivonás

    :param x:az első szám
    :type x: int
    :param y: a második szám
    :type y: int
    :return: eredmény
    :rtype: int

    """
    return x - y


def multiplication(x, y):
    """
    Szorzás

    :param x:az első szám
    :type x: int
    :param y: a második szám
    :type y: int
    :return: eredmény
    :rtype: int

    """
    return x * y


def division(x, y):
    """
    Osztás

    :param x:az első szám
    :type x: int
    :param y: a második szám
    :type y: int
    :return: eredmény
    :rtype: int

    """
    return x / y
